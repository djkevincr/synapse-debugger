package org.apache.synapse.util.streaming_xpath.exception;


public class StreamingXPATHException extends Exception {
    public StreamingXPATHException() {
        super();
    }

    public StreamingXPATHException(String message) {
        super(message);
    }

    public StreamingXPATHException(String message, Throwable throwable) {
        super(message, throwable);
    }

    public StreamingXPATHException(Throwable throwable) {
        super(throwable);
    }
}
